delimiter ;;
CREATE PROCEDURE `update_score`(in score int, in teamname varchar(255))
BEGIN
IF teamname not in (select 球队名称 from 参赛)
THEN
SIGNAL SQLSTATE 'HY000' SET MESSAGE_TEXT = '该球队未参赛';
ELSEIF score < 0
THEN 
SIGNAL SQLSTATE '22003' SET MESSAGE_TEXT = '进球数不合法';
ELSE
UPDATE 球队 SET 进球数 = score where 球队名称 = teamname; 
END IF;
END
;;
